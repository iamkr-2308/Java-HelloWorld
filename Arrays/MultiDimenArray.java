public class MultiDimenArray {
    public static void main(String[] args) {

        int[][] myNums = {{1,3,5,7,9}, {2,4,6,8,0}};
        System.out.println(myNums[0][0]);
        System.out.println(myNums[0][1]);
        System.out.println(myNums[0][2]);
        System.out.println(myNums[0][3]);
        System.out.println(myNums[0][4]);

        System.out.println("\n");
        
        System.out.println(myNums[1][0]);
        System.out.println(myNums[1][1]);
        System.out.println(myNums[1][2]);
        System.out.println(myNums[1][3]);
        System.out.println(myNums[1][4]);

    }
}
