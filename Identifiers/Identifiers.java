public class Identifiers {
    public static void main(String[] args) {

        int minutesPerHour = 60;
        System.out.println("Minutes in an hour: " + minutesPerHour);

        float totalmarks = 90.0f;
        System.out.println("Total Marks obtained : " + totalmarks); 
    }
}

// Identifiers - They come under variables, but Identifiers are properly defined variables. For eg; minutesPerHour, totalmarks, studentname.